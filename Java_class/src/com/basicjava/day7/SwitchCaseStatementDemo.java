package com.basicjava.day7;

public class SwitchCaseStatementDemo {
	
	public static void main(String[] args) {
		int data =20;
		switch (data) {
		
		case 1:
			System.out.println("Inside tase 1");
			break;
		case 9:
			System.out.println("Inside tase 9");
			break;
		default:
			System.out.println("Not valid case.");
		}
		
	}

}
